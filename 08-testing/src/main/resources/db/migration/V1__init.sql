DROP TABLE IF EXISTS FOODPRODUCT;

CREATE TABLE FOODPRODUCT (
  productname VARCHAR(100) PRIMARY KEY,
  CALORIES INT,
  SUGAR INT,
  FAT INT,
  PROTEIN INT,
  DOSIS INT
);

